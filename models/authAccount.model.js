const mongoose = require("mongoose");

const authAccountSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  },
);

const AuthAccount = mongoose.model("AuthAccount", authAccountSchema);

module.exports = AuthAccount;
